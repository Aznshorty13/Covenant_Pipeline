# ==========================================
# HELPER FUNCTIONS (Formatting & Cleaning)
# ==========================================
def compress_string(text: str) -> str:
    """
    The String Compression Rule.
    Aggressively strips all whitespace, newlines, and lowercases the string.
    This neutralizes EDGAR formatting artifacts to ensure our boundary detection
    is mathematically deterministic.
    """
    if pd.isna(text):
        return ""
    return re.sub(r'\s+', '', str(text)).lower()

def clean_footer_artifacts(text: str) -> str:
    """
    The Regex Scrub.
    Searches the extracted payload and permanently deletes physical page footers.
    This prevents token saturation and hallucinations in the Phase 1 LLM agents.
    """
    if not isinstance(text, str):
        return ""

    # (?i) makes it case-insensitive
    # \s* handles unpredictable spacing injected by the PDF conversion
    # [-–] captures both standard hyphens and en-dashes
    cleaned_text = re.sub(r'(?i)CREDIT AGREEMENT\s*[-–]\s*Page\s*\d+', '', text)

    # We run a final strip to ensure we don't leave floating empty lines at the end
    return cleaned_text.strip()

def calculate_printed_end_page(df: pd.DataFrame) -> pd.DataFrame:
    """
    Applies the sequential logic to calculate Printed_End_Page, handles the
    final document row mathematically, and explicitly reorders the dataframe columns.
    """
    df = df.copy()

    # 1. Vectorized Shift
    df['Printed_End_Page'] = df['Printed_Start_Page'].shift(-1)

    # 2. Handle the final row edge case mathematically
    last_idx = df.index[-1]
    abs_start = df.loc[last_idx, 'Absolute_Start_Page']
    abs_end = df.loc[last_idx, 'Absolute_End_Page']
    printed_start = df.loc[last_idx, 'Printed_Start_Page']

    page_delta = abs_end - abs_start
    df.loc[last_idx, 'Printed_End_Page'] = printed_start + page_delta

    # Clean up typing so it displays cleanly in the CSV (no floats)
    df['Printed_End_Page'] = df['Printed_End_Page'].astype(int)

    # 3. Column Reordering (Injecting right after Printed_Start_Page)
    cols = list(df.columns)
    cols.remove('Printed_End_Page')
    start_idx = cols.index('Printed_Start_Page')
    cols.insert(start_idx + 1, 'Printed_End_Page')

    return df[cols]

# ==========================================
# PASS 1: The Simplified TOC Skeleton
# ==========================================
def build_simplified_skeleton(pdf_path: str, max_toc_pages: int = 20) -> pd.DataFrame:
    print("Pass 1: Building TOC Skeleton...")
    doc = fitz.open(pdf_path)

    full_text_buffer = ""
    for page_num in range(min(max_toc_pages, len(doc))):
        full_text_buffer += doc[page_num].get_text("text") + "\n"

    clean_buffer = re.sub(r"\.{2,}", " ", full_text_buffer)
    clean_buffer = re.sub(r'","', ' ', clean_buffer).replace('"', '')

    toc_pattern = re.compile(
        r"(?P<type>Article|Section)\s+(?P<num>\d+(?:\.\d+)?)\.?\s+(?P<title>.{1,200}?)\s+(?P<page>\d+)(?=\n|$)",
        re.IGNORECASE | re.DOTALL
    )

    matches = toc_pattern.finditer(clean_buffer)

    skeleton_data = []
    current_article = "Preamble"
    current_article_title = "Document Front Matter"
    highest_page_seen = 0

    for match in matches:
        item_type = match.group("type").title().strip()
        num = match.group("num")
        title = match.group("title").replace("\n", " ").strip()
        page = int(match.group("page"))

        if page < (highest_page_seen - 2):
            print(f"   -> TOC boundary detected. Stopping scan.")
            break

        highest_page_seen = max(highest_page_seen, page)

        if item_type == "Article":
            current_article = f"Article {num}"
            current_article_title = title
        elif item_type == "Section":
            skeleton_data.append({
                "Article": current_article,
                "Article_Title": current_article_title,
                "Section": f"Section {num}",
                "Section_Title": title,
                "Printed_Start_Page": page
            })

    return pd.DataFrame(skeleton_data)


