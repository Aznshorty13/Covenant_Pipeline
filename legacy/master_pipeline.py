def load_pipeline_data(csv_path: str, json_path: str) -> Tuple[pd.DataFrame, Dict[str, Any], List[Dict[str, Any]]]:
    """Loads the raw text CSV and the compiled JSON payload."""
    try:
        df_raw = pd.read_csv(csv_path)
        with open(json_path, 'r') as file:
            compiled_data = json.load(file)
        covenants_list = compiled_data.get("Phase1_Extracted_Covenants", [])
        print(f"Successfully loaded CSV and {len(covenants_list)} compiled covenants.\n")
        return df_raw, compiled_data, covenants_list
    except FileNotFoundError as e:
        print(f"Error loading files: {e}. Please ensure files are in the working directory.")
        return pd.DataFrame(), {}, []

def build_rehydration_db(df_raw: pd.DataFrame) -> Dict[str, str]:
    """
    Reconstructs the composite Receipt string from the CSV to act as a primary key.
    Normalizes whitespace to prevent encoding drift during the join.
    """
    if df_raw.empty:
        return {}

    def build_receipt_string(row):
        return f"PDF Pages {row['Absolute_Start_Page']}-{row['Absolute_End_Page']} (Printed Pages {row['Printed_Start_Page']}-{row['Printed_End_Page']}) | {row['Article']}: {row['Article_Title']} | {row['Section']}: {row['Section_Title']}"

    df_raw['Reconstructed_Receipt'] = df_raw.apply(build_receipt_string, axis=1)

    # Strict normalization to prevent join failures
    df_raw['Reconstructed_Receipt'] = df_raw['Reconstructed_Receipt'].astype(str).str.strip().str.replace(r'\s+', ' ', regex=True)

    return dict(zip(df_raw['Reconstructed_Receipt'], df_raw['Raw_Text']))

